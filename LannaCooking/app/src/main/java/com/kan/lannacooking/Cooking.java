public class Cooking {
    private int c_Id;
    private String c_Name;
    private String c_item;
    private String c_step;
    private String c_tips;

    public int getC_Id() {
        return c_Id;
    }

    public void setC_Id(int c_Id) {
        this.c_Id = c_Id;
    }

    public String getC_Name() {
        return c_Name;
    }

    public void setC_Name(String c_Name) {
        this.c_Name = c_Name;
    }

    public String getC_item() {
        return c_item;
    }

    public void setC_item(String c_item) {
        this.c_item = c_item;
    }

    public String getC_step() {
        return c_step;
    }

    public void setC_step(String c_step) {
        this.c_step = c_step;
    }

    public String getC_tips() {
        return c_tips;
    }

    public void setC_tips(String c_tips) {
        this.c_tips = c_tips;
    }
}
